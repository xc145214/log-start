package com.xc.concurrent.sync;

/**
 *  同步方法。
 *
 *  @author xiachuan at 2016/4/29 15:50。
 */

public class SynchronizedCounter {

    private int c = 0;

    public synchronized void increment() {
        c++;
    }

    public synchronized void decrement() {
        c--;
    }

    public synchronized int value() {
        return c;
    }
}

